@app.route("/chat", methods=["GET"])
def chat():
    q = request.args.get("q", "")
    if not q:
        return jsonify({"status": "error", "message": "استخدم ?q=سؤالك"}), 400

    session_id = request.args.get("session_id", str(uuid.uuid4())[:8])

    # استرجاع أو إنشاء جلسة
    if session_id not in SESSIONS:
        SESSIONS[session_id] = []
        # إضافة البرومبت السياقي كرسالة system في بداية الجلسة
        SESSIONS[session_id].append({"role": "system", "content": SYSTEM_PROMPT})

    memory = SESSIONS[session_id]
    memory.append({"role": "user", "content": q})

    try:
        reply = call_deepseek(memory)
        if not reply:
            return jsonify({"status": "error", "message": "لم يتم استلام رد من DeepSeek"}), 500

        memory.append({"role": "assistant", "content": reply})
        SESSIONS[session_id] = memory

        return jsonify({
            "status": "success",
            "reply": reply,
            "session_id": session_id,
            "owner": MY_OWNER,
            "channel": MY_CHANNEL,
            "developer": MY_NAME
        })
    except Exception as e:
        if memory and memory[-1]["role"] == "user":
            memory.pop()
        return jsonify({"status": "error", "message": f"فشل الاتصال: {str(e)}"}), 500

